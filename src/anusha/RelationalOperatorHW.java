package anusha;

public class RelationalOperatorHW {
    public static void main(String[] args) {
    	int a=12;
    	int b=24;
    	
    	if(a>b)
    		System.out.println("as a is greater than b, performing multiplication a*b----"+ a*b);
    	if(a<b)
    		System.out.println("as b is greater than a, performing division b/a----"+ b/a);
    	if(a==b)
    		System.out.println("as a and b are equal we perform bit shift a>>b----"+ (a>>b));
    }
}
